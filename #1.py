#1.	Faça um programa que receba 5 notas, calcule a média aritmética destas notas e apresente o resultado.

nota1 = float(input("Digite a Primeira nota: "))
nota2 = float(input("Digite a Segunda nota: "))
nota3 = float(input("Digite a Terceira nota: "))
nota4 = float(input("Digite a Quarta nota: "))
nota5 = float(input("Digite a Quinta nota: "))

soma = (nota1+nota2+nota3+nota4+nota5)

media = soma/5

print('A média aritimética é: ',media)